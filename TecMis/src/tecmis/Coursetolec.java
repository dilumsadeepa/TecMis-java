    private void claddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_claddActionPerformed
        // TODO add your handling code here:
        String cid = clcid.getText();
        String lid = cllid.getText();
        
        try {
            
            if(cid.equals("") || lid.equals("")){
                cltext.setText("Error in getting data");
                
            }else{
                
                   String sql = "insert into courselecs (cl_cid,cl_lid) values ('"+cid+"','"+lid+"')";
                
                    int res = db.stm.executeUpdate(sql);
                    if (res > 0) {
                        cltext.setText("Lecture added to course Successfully");
                        DefaultTableModel dtm = (DefaultTableModel)cltable.getModel();
                        
                        String[] items = {cid,lid};
                        dtm.addRow(items);
                        
                        clcid.setText("");
                        cllid.setText("");
                        
                        
                    }else{
                        cltext.setText("database error");
                   
            }
            }
        } catch (Exception e) {
            cltext.setText("Did not run try");
        }
    }//GEN-LAST:event_claddActionPerformed

    private void cldeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cldeleteActionPerformed
        // TODO add your handling code here:
        int drow = cltable.getSelectedRow();
        
        if(drow > 0){
            String ccid = (String) cltable.getValueAt(drow, 0);
            String clid = (String) cltable.getValueAt(drow, 1);
                       
            DefaultTableModel model = (DefaultTableModel)cltable.getModel();
            model.removeRow(drow);
            
            String dsql = "delete from courselecs where cl_cid ='"+ccid+"' and cl_lid = '"+clid+"'";
            
            try {
                int rest = db.stm.executeUpdate(dsql);
                if(rest > 0){
                    cltext.setText("Deleted successfully");
                }else{
                    cltext.setText("Deleted Unsuccessfully");
                }
                
            } catch (SQLException ex) {
                Logger.getLogger(Adduseradmin.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else{
            cltext.setText("Select a row");
        }
    }//GEN-LAST:event_cldeleteActionPerformed

