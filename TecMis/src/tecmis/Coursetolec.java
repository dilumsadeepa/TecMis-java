    public void datatota(){
        try {
            DefaultTableModel model = (DefaultTableModel)cltable.getModel();
            
            String qu = "SELECT * FROM courselecs";
            ResultSet rs = db.stm.executeQuery(qu);
            
            while(rs.next()){
                Object[] row = {rs.getString("cl_cid"),rs.getString("cl_lid")};
                model.addRow(row);
                
            }
            
            
            
        } catch (SQLException ex) {
            Logger.getLogger(Adduseradmin.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

