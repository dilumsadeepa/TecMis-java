    private void loginbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginbtnActionPerformed
        
        String luser = lusername.getText();
        String lpwd = lpassword.getText();
        
        DB db = new DB();
        db.getconnect();
        
        try {
            
            String sql = "select * from users where email = '"+luser+"' and password = '"+lpwd+"'";
            
            ResultSet result = db.stm.executeQuery(sql);

           if(result.next()){
               dispose();
               int level = result.getInt("level");
               int id = result.getInt("id");
               if(level == 1){
                   Admindashboard admin = new Admindashboard(id);
                   admin.show();
               }else if(level == 2){
                   Techoffidashb toff = new Techoffidashb(id);
                   toff.show();
               }else if(level == 3){
                   //lecturedashboard lec = new lecturedashboard(id);
                   //lec.show();
               }else if(level == 4){
                   Studentdashbord stu = new Studentdashbord();
                   stu.show();
               }
           }else{
               JOptionPane.showMessageDialog(this, "The username or passsword is incorrect");
           }
        } catch (SQLException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
            
            
         
    }//GEN-LAST:event_loginbtnActionPerformed

